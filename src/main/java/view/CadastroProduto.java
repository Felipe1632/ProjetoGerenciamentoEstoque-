    private void cadastrarprodutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarprodutActionPerformed
String nome = nomeproduto.getText().trim();
String precoTexto = precoproduto.getText().trim();
String estoqueTexto = estoqueproduto.getText().trim();

if (nome.isEmpty() || precoTexto.isEmpty() || estoqueTexto.isEmpty()) {

    JOptionPane.showMessageDialog(null, "Preencha todos os campos!");

} else {

    try {

        double preco = Double.parseDouble(precoTexto);
        int estoque = Integer.parseInt(estoqueTexto);

        ProdutoBean produto = new ProdutoBean();
        produto.setNome(nome);
        produto.setPreco(preco);
        produto.setEstoque(estoque);

        ProdutoDAO dao = new ProdutoDAO();
        dao.cadastrarProduto(produto);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Preço e estoque devem ser números válidos!");
    }
}
    }//GEN-LAST:event_cadastrarprodutActionPerformed

